def create_signal (mass_1):
    h16_data = []
    unsign_data = []   
    sign_data   = []
    mass_1 = mass_1.replace('\n',',')
    mass_1 = mass_1.replace('0x','')
    mass_1 = mass_1.split(',')
    for i in range(4,len(mass_1),2):
        d = mass_1[i+1]+ mass_1[i]
        h16_data.append(d)    
        d = bytes.fromhex(d)
        d = int.from_bytes(d,byteorder = 'big')
        sign_data.append(d)
        unsign_data.append(d)

        for i in range(len(sign_data)):
            if sign_data[i]>=32768:
                sign_data[i] = sign_data[i] - 2**16
    print('hex ' , h16_data, len(h16_data))
    print('uint ',unsign_data,len(unsign_data))
    print('sint ',sign_data,len(sign_data))
    print('')

